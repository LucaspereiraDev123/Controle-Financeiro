        <h2>2. Dados que coletamos</h2>
        <ul>
            <li><strong>Dados de cadastro:</strong> nome e endereço de e-mail.</li>
            <li><strong>Dados de autenticação:</strong> senha (armazenada apenas de forma criptografada,
                com hash — nunca em texto puro) e data de verificação do e-mail.</li>
            <li><strong>Dados financeiros que você cadastra:</strong> as receitas, despesas, valores,
                descrições e categorias que você registra para controlar suas finanças. Esses dados
                são inseridos por você e usados exclusivamente para lhe apresentar seus próprios saldos
                e relatórios.</li>
            <li><strong>Dados de assinatura e pagamento:</strong> a situação da sua assinatura, as datas
                de início e término do período de teste e um identificador da assinatura no processador
                de pagamento. <strong>Os dados do cartão são digitados e processados diretamente pelo
                Mercado Pago</strong> — o Aí Economiza não recebe, vê nem armazena o número do seu cartão.</li>
            <li><strong>Dados técnicos e de acesso:</strong> registros (logs) que podem incluir endereço IP,
                data e hora de acesso e informações do navegador, gerados automaticamente para segurança e
                funcionamento do Serviço.</li>
            <li><strong>Registro de consentimento:</strong> data e hora do aceite dos Termos de Uso e
                desta Política.</li>
        </ul>

        <h2>3. Cookies</h2>
        <p>
            Utilizamos apenas <strong>cookies estritamente necessários</strong> ao funcionamento do
            Serviço — por exemplo, para manter você autenticado durante a sessão e proteger formulários
            contra fraude (CSRF). Não usamos cookies de publicidade nem de rastreamento de terceiros.
        </p>

        <h2>4. Para que usamos seus dados</h2>
        <ul>
            <li>Criar e manter a sua conta e autenticar o seu acesso;</li>
            <li>Fornecer as funcionalidades de controle financeiro (registro e visualização de
                receitas, despesas, categorias e saldos);</li>
            <li>Processar sua assinatura e liberar/bloquear o acesso conforme o pagamento;</li>
            <li>Enviar e-mails essenciais ao serviço (verificação de conta e redefinição de senha);</li>
            <li>Prevenir fraudes, garantir a segurança e a integridade da plataforma;</li>
            <li>Cumprir obrigações legais e regulatórias.</li>
        </ul>

        <h2>5. Base legal</h2>
        <p>
            O tratamento dos seus dados se fundamenta, conforme o art. 7º da LGPD, na
            <strong>execução do contrato</strong> (prestação do serviço que você contratou), no
            <strong>consentimento</strong> manifestado no cadastro, no atendimento ao
            <strong>legítimo interesse</strong> para segurança e prevenção a fraudes, e no
            <strong>cumprimento de obrigações legais</strong>.
        </p>

        <h2>6. Com quem compartilhamos</h2>
        <p>
            <strong>Nós não vendemos seus dados pessoais.</strong> Compartilhamos informações apenas
            com prestadores de serviço indispensáveis à operação, e somente no necessário:
        </p>
        <ul>
            <li><strong>Processamento de pagamentos:</strong> Mercado Pago, para cobrança e gestão da
                assinatura;</li>
            <li><strong>Envio de e-mails:</strong> provedor de e-mail transacional (Brevo), para as
                mensagens de verificação e redefinição de senha;</li>
            <li><strong>Infraestrutura / hospedagem:</strong> provedor responsável por hospedar a
                aplicação e o banco de dados.</li>
        </ul>
        <p>
            Esses fornecedores podem operar servidores fora do Brasil. Nesses casos, a transferência
            internacional observa as salvaguardas exigidas pela LGPD. Também poderemos divulgar dados
            quando exigido por lei ou por autoridade competente.
        </p>

        <h2>7. Retenção dos dados</h2>
        <p>
            Mantemos seus dados enquanto a sua conta estiver ativa. Após o encerramento da conta,
            eliminamos ou anonimizamos os dados pessoais, salvo quando a manutenção for necessária para
            cumprir obrigação legal, exercer direitos em processo ou por outra hipótese autorizada pela LGPD.
        </p>

        <h2>8. Segurança</h2>
        <p>
            Adotamos medidas técnicas e organizacionais para proteger seus dados, como criptografia de
            senha (hash), isolamento dos dados por usuário (cada conta acessa apenas os próprios dados),
            controle de acesso e comunicação protegida. Nenhum sistema é totalmente imune a incidentes;
            em caso de incidente de segurança relevante, adotaremos as providências e comunicações
            previstas na LGPD.
        </p>

        <h2>9. Seus direitos como titular</h2>
        <p>Nos termos do art. 18 da LGPD, você pode, a qualquer momento, solicitar:</p>
        <ul>
            <li>Confirmação da existência de tratamento e acesso aos seus dados;</li>
            <li>Correção de dados incompletos, inexatos ou desatualizados;</li>
            <li>Anonimização, bloqueio ou eliminação de dados desnecessários ou tratados em desconformidade;</li>
            <li>Portabilidade dos dados a outro fornecedor, mediante requisição;</li>
            <li>Eliminação dos dados tratados com base no consentimento;</li>
            <li>Informação sobre com quem compartilhamos seus dados;</li>
            <li>Revogação do consentimento.</li>
        </ul>
        <p>
            Para exercer esses direitos, escreva para
            <a href="mailto:privacidade@aieconomiza.com.br">privacidade@aieconomiza.com.br</a>.
            Você também tem o direito de peticionar perante a Autoridade Nacional de Proteção de
            Dados (ANPD).
        </p>

        <h2>10. Crianças e adolescentes</h2>
        <p>
            O Aí Economiza não é destinado a menores de 18 anos e não coletamos intencionalmente dados
            de crianças e adolescentes. Caso identifiquemos esse tratamento sem a devida autorização,
            os dados serão eliminados.
        </p>

